## Safe Mode In MYSQL

    - it is the safe mode by default on (1) in our MYSQL system that prevent us to update any changes in DataBase

    - it is made for the secuirty purpose so that that can't be automattically can be changed



    - to update in any your table or db

        - step 01   remove your safe mode
        - step 02  SET_SAFE_UPDATES = 0 (off);


    - to safe mode 

        1. on       
                SET_SAFE_UPDATES = 1;

        2. Off
                SET_SAFE_UPDATES = 0;

